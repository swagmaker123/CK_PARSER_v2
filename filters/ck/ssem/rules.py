    ("цифровые финансовые активы", r"цифров\w+\s+финансов\w+\s+актив"),
    ("цифровые активы", r"цифров\w+\s+актив"),
    ("цифровая валюта", r"цифров\w+\s+валют"),
    ("стейблкоины", r"стейблкоин|stablecoin"),
    ("цифровой рубль", r"цифров\w+\s+рубл"),
