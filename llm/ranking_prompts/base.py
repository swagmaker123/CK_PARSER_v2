import json

from llm.config import TEXT_TRUNCATE


class RankingPromptProfile:
    def __init__(self, ck_id, ck_title, auditor_role, relevant_rules, weak_rules, topics):
        self.ck_id = ck_id
        self.ck_title = ck_title
        self.auditor_role = auditor_role
        self.relevant_rules = relevant_rules
        self.weak_rules = weak_rules
        self.topics = topics


def _bullet_list(items):
    return "\n".join(f"- {item}" for item in items)


def _compact(value, limit=None):
    text = str(value or "").strip()
    if limit is not None:
        return text[:limit]
    return text


def build_scoring_prompt(profile, row):
    title = _compact(row.get("Заголовок статьи", ""))
    source = _compact(row.get("Источник", ""))
    date = _compact(row.get("Дата новости", ""))
    ck_name = _compact(row.get("Наименование ЦК", profile.ck_title))
    keywords = _compact(row.get("Ключевые слова", ""))
    text = _compact(row.get("Полный текст", ""), TEXT_TRUNCATE)

    return f"""Ты — {profile.auditor_role}.

Оцени одну новость НЕ по общей важности, а по полезности для дальнейшего отбора в топ-10 новостей для аудиторского мониторинга по направлению "{profile.ck_title}".

Нас интересуют новости, из которых потенциально можно вывести направление проверки, контрольный вопрос, изменение процесса или новый риск для банка.

Считать релевантным:
{_bullet_list(profile.relevant_rules)}

Считать нерелевантным или слабым:
{_bullet_list(profile.weak_rules)}

Новость:
Дата: {date}
Источник: {source}
Наименование ЦК: {ck_name}
Заголовок: {title}
Ключевые слова фильтра: {keywords}
Текст:
{text}

Верни строго валидный JSON без markdown и без текста вне JSON.

Формат:
{{
  "score": 0,
  "is_candidate": false,
  "topic": "not_relevant",
  "summary": "выжимка новости для ленты: 2-3 предложения живым языком",
  "reason": "кратко почему такая оценка"
}}

Где:
- score — оценка 0-100 именно для отбора в топ-10 по аудиторскому мониторингу направления "{profile.ck_title}";
- is_candidate — true, если новость стоит передать во второй этап выбора топа;
- topic — одна категория из списка: {", ".join(f'"{topic}"' for topic in profile.topics)};
- summary — готовый текст для новостной ленты и рассылки: 2-3 связных предложения, которые передают суть новости по теме "{profile.ck_title}". Пиши как редактор деловой ленты, а не как аудитор;
- reason — одно короткое предложение, почему новость получила такую оценку (только для reason допустима аудиторская логика; в summary её быть не должно).

Шкала score:
90-100 — прямое регуляторное изменение, позиция регулятора или обязательное изменение процесса; почти точно кандидат в топ-10. В особенности новости связанные со Сбер/Сбербанк.
75-89 — сильная практика банка, пилот или рыночное изменение, из которого можно вывести направление проверки.
55-69 — умеренно полезно, но обычно не передавать во второй этап, если есть более сильные новости.
30-54 — слабая связь с направлением "{profile.ck_title}"; обычно не кандидат.
0-29 — нерелевантно или ложное срабатывание фильтра.

Правила:
- Если score >= 60, is_candidate = true.
- Если score < 60, is_candidate = false.
- summary заполняй даже для нерелевантных новостей, чтобы строка оставалась понятной в Excel.
- summary — это выжимка самой новости, а не комментарий аудитора. Изложи факты: кто сообщил, что произошло или меняется, когда и в чём суть. Не объясняй, какие проверки, риски, требования к комплаенсу или отчётности это создаст для банков.
- В summary не используй роботизированные конструкции и шаблоны: "это создаст новые требования", "потребует от банков", "актуально для мониторинга", "может повлиять на процессы", "важно для направления проверки", "следует учитывать при аудите".
- Не дублируй заголовок и не добавляй второе предложение с оценкой важности или последствий — summary должен читаться как цельный абзац новостной ленты.
- Плохо: "ЦБ опубликовал проект положения о цифровом рубле. Это создаст новые требования к комплаенсу и отчётности для банков и клиентов."
- Хорошо: "Банк России опубликовал проект положения о порядке обращения цифрового рубля. Документ описывает правила открытия счетов, проведения операций и взаимодействия банков с платформой цифрового рубля."
- Не завышай score новости только потому, что она важная для экономики в целом. Оцени только применимость к аудиторскому мониторингу направления "{profile.ck_title}".
- Дайджесты, обзоры и подборки нескольких новостей оценивай низко: обычно score не выше 45. Для топа предпочтительна отдельная новость про одно конкретное событие. Исключение — если дайджест содержит уникальную важную информацию, которой нет в отдельной новости."""


def build_ranking_prompt(profile, candidates, top_n=10, reserve_n=5):
    candidate_items_json = json.dumps(
        candidates,
        ensure_ascii=False,
        indent=2,
    )
    total_n = top_n + reserve_n

    return f"""Ты — руководитель аудиторского мониторинга банка по направлению "{profile.ck_title}".

Ниже список новостей-кандидатов после первичного скоринга. Нужно выбрать ранжированный список из {total_n} новостей:
- первые {top_n} — основной топ для аудиторов;
- позиции {top_n + 1}-{total_n} — резерв на случай ручного исключения или дублей.

Твоя задача — выбрать новости, которые лучше всего помогают определить будущие или текущие направления проверки банка по направлению "{profile.ck_title}".

Критерии отбора:
1. Выше ставь новости про регуляторные изменения, требования регуляторов, обязательные процессы, новые риски и инфраструктурные изменения.
2. Выше ставь новости, из которых можно прямо сформулировать проверку процесса, контроля, тарифа, договора, клиентского раскрытия, API, безопасности, комплаенса или операционной устойчивости.
3. Не выбирай новости только из-за высокой общей важности, если они не дают аудиторского направления по "{profile.ck_title}".
4. Не ставь два дубля одного события в основной топ-{top_n}. Если несколько новостей описывают одно событие, выбери наиболее полную, первичную или юридически точную.
5. Дайджесты, обзоры и подборки нескольких новостей не включай в основной топ-{top_n}, если есть отдельная новость про то же событие. Их можно оставить только в резерве или исключить.
6. Сохраняй разумное разнообразие тем, если есть несколько сильных новостей по разным подтемам.
7. Если новость хорошая, но менее сильная или похожа на уже выбранную, поставь её в резерв {top_n + 1}-{total_n}.

Входные данные:
{candidate_items_json}

Каждый кандидат содержит:
- row_id — номер строки/идентификатор новости;
- date — дата новости;
- source — источник;
- title — заголовок;
- keywords — ключевые слова фильтра;
- score — первичная оценка;
- topic — тема;
- short_summary — краткое резюме новости;
- reason — причина первичной оценки.

Верни строго валидный JSON без markdown и без текста вне JSON.

Формат:
{{
  "ranked_top15": [
    {{
      "rank": 1,
      "row_id": 0,
      "title": "заголовок новости",
      "source": "источник",
      "score": 95,
      "topic": "digital_ruble",
      "why_selected": "почему новость попала в рейтинг",
      "audit_angle": "какое направление проверки можно вывести"
    }}
  ],
  "possible_duplicates_in_top15": [
    {{
      "row_ids": [0, 1],
      "reason": "почему эти новости могут быть дублями"
    }}
  ],
  "excluded_strong_candidates": [
    {{
      "row_id": 0,
      "title": "заголовок новости",
      "reason": "почему сильный кандидат не попал в top15"
    }}
  ]
}}

Правила:
- ranked_top15 должен содержать не больше {total_n} новостей.
- Если кандидатов меньше {total_n}, верни столько, сколько есть.
- Первые {top_n} позиций ranked_top15 считаются основным топом.
- Позиции {top_n + 1}-{total_n} считаются резервом.
- Не придумывай row_id, title, source или score — используй только данные из входного списка.
- possible_duplicates_in_top15 заполняй только если реально видишь похожие события.
- Если дублей нет, верни пустой массив."""
